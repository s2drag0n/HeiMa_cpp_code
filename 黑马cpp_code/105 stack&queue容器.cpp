


/*
* stack栈是一种先进后出FILO的数据结构，只有一个出口
* 
* 默认构造
* 拷贝构造
* 
* 赋值使用=
* 
* 入栈push(elem)
* 出栈pop()
* 判断是否为空empty()
* 返回元素个数size()
* 返回栈顶元素top()
* 
* 不允许遍历行为
*/

/*
* queue队列是一种先进先出FIFO的数据结构，有两个出口
* 
* 默认构造
* 拷贝构造
* 
* 赋值使用=
* 
* 队尾入push(elem)
* 队头出pop()
* 队尾数据back()
* 队头数据front()
* 判断是否为空empty()
* 返回元素个数size()
* 
*/